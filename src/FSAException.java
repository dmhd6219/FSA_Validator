/**
 * The {@link Exception} for all FSA errors.
 */
public class FSAException extends Exception {
    /**
     * Instantiates a new FSA exception.
     */
    FSAException() {
        super();
    }

    /**
     * Instantiates a new FSA exception.
     *
     * @param s the error message
     */
    FSAException(String s) {
        super(s);
    }

}
